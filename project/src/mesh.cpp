// load object
void Mesh::loadObject(const char* location) {

	// open file
	FILE * file = fopen(location, "r");
	
	// check if you can open file
	if (file == NULL) {
		std::cout << "unable to open .obj file! [CANT LOAD OBJECT]" << std::endl;
		return;
	}
	
	// create temp vectors
	std::vector< unsigned int > vertexIndices, textureIndices, normalIndices;
	std::vector<glm::vec3> temp_vertices;
	std::vector<glm::vec2> temp_textures;
	std::vector<glm::vec3> temp_normals;

	// while loop thats reads the files
	while (1) {
		char lineHeader[128];

		// read the first word of the line
		int res = fscanf(file, "%s", lineHeader);

		// check if at end of lines
		if (res == EOF) { // EOF = end of line
			break;
		}

		// read line and proces information
		if (strcmp(lineHeader, "v") == 0) {
			glm::vec3 vertex;
			fscanf(file, "%f %f %f\n", &vertex.x, &vertex.y, &vertex.z);
			temp_vertices.push_back(vertex);
		}
		else if (strcmp(lineHeader, "vt") == 0) {
			glm::vec2 uv;
			fscanf(file, "%f %f\n", &uv.x, &uv.y);
			temp_textures.push_back(uv);
		}
		else if (strcmp(lineHeader, "vn") == 0) {
			glm::vec3 normal;
			fscanf(file, "%f %f %f\n", &normal.x, &normal.y, &normal.z);
			temp_normals.push_back(normal);
		}
		else if (strcmp(lineHeader, "f") == 0) {
			std::string vertex1, vertex2, vertex3;
			unsigned int vertexIndex[3], uvIndex[3], normalIndex[3];
			int matches = fscanf(file, "%d/%d/%d %d/%d/%d %d/%d/%d\n", &vertexIndex[0], &uvIndex[0], &normalIndex[0], &vertexIndex[1], &uvIndex[1], &normalIndex[1], &vertexIndex[2], &uvIndex[2], &normalIndex[2]);
			if (matches != 9) {
				std::cout << "File can't be read by our simple parser [Try exporting with other options]" << std::endl;
				return;
			}
			vertexIndices.push_back(vertexIndex[0]);
			vertexIndices.push_back(vertexIndex[1]);
			vertexIndices.push_back(vertexIndex[2]);
			textureIndices.push_back(uvIndex[0]);
			textureIndices.push_back(uvIndex[1]);
			textureIndices.push_back(uvIndex[2]);
			normalIndices.push_back(normalIndex[0]);
			normalIndices.push_back(normalIndex[1]);
			normalIndices.push_back(normalIndex[2]);
		}
	}

	// create the vertices
	GLfloat *vertices = new GLfloat[vertexIndices.size() * 8];
	for (unsigned int i = 0; i<vertexIndices.size(); i++) {
		// create vertices
		unsigned int vertexIndex = vertexIndices[i];
		vertices[i * 8] = temp_vertices[vertexIndex - 1].x;
		vertices[i * 8 + 1] = temp_vertices[vertexIndex - 1].y;
		vertices[i * 8 + 2] = temp_vertices[vertexIndex - 1].z;
		unsigned int normalIndex = normalIndices[i];
		vertices[i * 8 + 3] = temp_normals[normalIndex - 1].x;
		vertices[i * 8 + 4] = temp_normals[normalIndex - 1].y;
		vertices[i * 8 + 5] = temp_normals[normalIndex - 1].z;
		unsigned int uvIndex = textureIndices[i];
		vertices[i * 8 + 6] = temp_textures[uvIndex - 1].x;
		vertices[i * 8 + 7] = temp_textures[uvIndex - 1].y;
	}

	// set draw size to how many points there are
	drawsize = vertexIndices.size() * 8 * sizeof(GLfloat);
	std::cout << drawsize << std::endl;

	// create buffer
	glGenVertexArrays(1, &VAO);
	glGenBuffers(1, &VBO);
	glBindVertexArray(VAO);
	glBindBuffer(GL_ARRAY_BUFFER, VBO);
	glBufferData(GL_ARRAY_BUFFER, drawsize, vertices, GL_STATIC_DRAW);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid *)0);
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid *)(3 * sizeof(GLfloat)));
	glEnableVertexAttribArray(1);
	glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(GLfloat), (GLvoid *)(6 * sizeof(GLfloat)));
	glEnableVertexAttribArray(2);
	glBindVertexArray(0);
	
	// close file
	fclose(file);
}